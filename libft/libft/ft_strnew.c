#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*new;

	if ((size_t)(-1) == size)
		return (NULL);
	if (!(new = ft_memalloc(size + 1)))
		return (NULL);
	new[size] = '\0';
	return (new);
}
